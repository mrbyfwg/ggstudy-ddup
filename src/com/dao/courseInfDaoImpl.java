package com.dao;

import com.bean.CourseinformationEntity;
import org.springframework.transaction.annotation.Transactional;

import java.util.List;

public class courseInfDaoImpl extends BaseHibernateDao implements courseInfDao{

    public courseInfDaoImpl(){
        System.out.println("create courseInfDaoImpl.");
    }

    @Override
    @Transactional
    public List findByHql(String hql) {
        try{
            return getSession().createQuery(hql).list();
        }catch (RuntimeException re){
            System.out.println("findByHql error!");
            throw re;
        }
    }
    @Override
    @Transactional
    public CourseinformationEntity findById(String id) {
        try{
            return (CourseinformationEntity) getSession().get(CourseinformationEntity.class, id);
        }catch (RuntimeException re){
            System.out.println("findById error");
            throw re;
        }
    }
    @Override
    @Transactional
    public void save(CourseinformationEntity t) {
        try {
            getSession().save(t);
        } catch (RuntimeException re) {
            System.out.println("save error");
            throw re;
        }
    }
    @Override
    @Transactional
    public void update(CourseinformationEntity t) {
        try {
            getSession().update(t);
        } catch (RuntimeException re) {
            System.out.println("update error");
            throw re;
        }
    }
    @Override
    @Transactional
    public void delete(CourseinformationEntity t) {
        try {
            getSession().delete(t);
        } catch (RuntimeException re) {
            System.out.println("delete error");
            throw re;
        }
    }
}
